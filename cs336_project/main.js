
function start(){

    var h = window.innerHeight;
    var w = window.innerWidth;

    var aspectRatio = w/h;
    var fieldOfView = 30;
    var nearPlane = 0.1;
    var farPlane = 1000;

    var ourCanvas = document.getElementById('theCanvas');
    var renderer = new THREE.WebGLRenderer({canvas: ourCanvas, alpha: true, antialias: true});
    document.body.appendChild(renderer.domElement)
    renderer.setSize(w,h);

    var scene = new THREE.Scene();
    var camera = new THREE.PerspectiveCamera( fieldOfView, aspectRatio, nearPlane, farPlane );
    camera.position.x = 2;
    camera.position.y = 2;
    camera.position.z = 5;
    camera.lookAt(new THREE.Vector3(0, 0, 0));

    //Camera controls
    var controls = new THREE.OrbitControls(camera, renderer.domElement);
    controls.update();


    //OBEJCTS below

    //light
    var light = new THREE.AmbientLight(0xfffffffff, .8);
    scene.add(light);

    //backlight
    var backLight = new THREE.DirectionalLight(0xfffffffff, .2);
    backLight.position.set(-100, 200, 50);
    scene.add(backLight);

    //cube
	//generateChunk(scene, 5);
	cube(scene, 1,0,0);
	cube(scene, 2,0,0);
	cube(scene, 3,0,0);

    
    var render = function(){
        requestAnimationFrame(render);
        //put code here
        renderer.render(scene, camera);
    };
    render();
}

