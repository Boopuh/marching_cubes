function generateEdgeCoordFromCornerVertice(vec1, vec2){
    return new THREE.Vector3(
            vec1.x + ((vec2.x - vec1.x)/2.0), 
            vec1.y + ((vec2.y - vec1.y)/2.0), 
            vec1.z + ((vec2.z - vec1.z)/2.0)
        ) 
}

function generateEdgesCoordArray(vects){
    return [
        generateEdgeCoordFromCornerVertice(vects[3], vects[6]),//0
        generateEdgeCoordFromCornerVertice(vects[2], vects[3]),//1
        generateEdgeCoordFromCornerVertice(vects[2], vects[7]),//2
        generateEdgeCoordFromCornerVertice(vects[7], vects[6]),//3
        generateEdgeCoordFromCornerVertice(vects[1], vects[4]),//4
        generateEdgeCoordFromCornerVertice(vects[0], vects[1]),//5
        generateEdgeCoordFromCornerVertice(vects[0], vects[5]),//6
        generateEdgeCoordFromCornerVertice(vects[5], vects[4]),//7
        generateEdgeCoordFromCornerVertice(vects[4], vects[6]),//8
        generateEdgeCoordFromCornerVertice(vects[1], vects[3]),//9
        generateEdgeCoordFromCornerVertice(vects[0], vects[2]),//10
        generateEdgeCoordFromCornerVertice(vects[5], vects[7]),//11
    ]
}

function drawDot(scene, vec3){
    var geometry = new THREE.SphereGeometry( .025, 32, 32 );
    var material = new THREE.MeshLambertMaterial( {color: 0xffff00} );
    var sphere = new THREE.Mesh( geometry, material );
    sphere.position.x  = vec3.x;
    sphere.position.y = vec3.y;
    sphere.position.z = vec3.z;
    scene.add( sphere );
}

function makeBetterTriangleTable(array){
    //console.log(array);
    var index = 0;
    var outer = [];
    for(var i = 0; i < 256; i++){
        var inner = [];
        for(var j = 0; j < 16; j++){
            inner.push(array[index]);
            index++;
        }
        outer.push(inner);
    }
    return outer;
}

